    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        int respuesta = 0;
        String valor_clave = new String(txt_clave.getPassword());
        try {
            String sentencia = "{CALL ?:= PK_BASICAS.F_LOGIN(?,?)}";
            comando = Conexion_base.coneccion.prepareCall(sentencia);
            comando.registerOutParameter(1, Types.INTEGER);
            comando.setString(2, txt_usuario.getText());
            comando.setString(3, valor_clave);
            comando.execute();
            respuesta = (int) comando.getObject(1);
            if (respuesta == 1) {
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "ERROR: USUARIO/CLAVE INCORRECTA");
            }
            comando.close();
        } catch (SQLException | HeadlessException e) {
        }
    }//GEN-LAST:event_btn_loginActionPerformed

